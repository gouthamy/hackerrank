package HourRank16;

import java.util.Arrays;
import java.util.Scanner;

public class CandyPiles {

	
	 public static void main(String[] args) {
	        Scanner in = new Scanner(System.in);
	        int n = in.nextInt();
	        int[] c = new int[n];
	        for(int c_i=0; c_i < n; c_i++){
	            c[c_i] = in.nextInt();
	        }
	        Arrays.sort(c);
	        
	        
	        
	    }
}
